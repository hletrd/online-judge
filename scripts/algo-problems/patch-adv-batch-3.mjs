async function fetchAllProblems() {
  let allProblems = [];
  let page = 1;
  while (true) {
    const res = await fetch(`${API_BASE}/api/v1/problems?page=${page}&limit=50`, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) throw new Error(`GET page ${page} failed: ${res.status}`);
    const data = await res.json();
    const items = data.problems ?? data.data ?? data;
    if (!Array.isArray(items) || items.length === 0) break;
    allProblems = allProblems.concat(items);
    if (items.length < 50) break;
    page++;
  }
  return allProblems;
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function patchProblem(id, description) {
  const res = await fetch(`${API_BASE}/api/v1/problems/${id}`, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ description }),
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`PATCH ${id} failed: ${res.status} ${text}`);
  }
  return res.json();
}

async function main() {
  console.log('Fetching all problems from API...');
  const apiProblems = await fetchAllProblems();
  console.log(`Total problems fetched: ${apiProblems.length}`);

  // Build title → id map
  const titleToId = new Map();
  for (const p of apiProblems) {
    titleToId.set(p.title, p.id ?? p._id);
  }

  let matched = 0;
  let skipped = 0;
  const errors = [];

  for (let i = 0; i < problems.length; i++) {
    const prob = problems[i];
    const newDesc = naturalizedDescriptions[i];
    const id = titleToId.get(prob.title);

    if (!id) {
      console.warn(`[${i + 1}] SKIP (not found): "${prob.title}"`);
      skipped++;
      continue;
    }

    try {
      await patchProblem(id, newDesc);
      console.log(`[${i + 1}] PATCHED: "${prob.title}" (id=${id})`);
      matched++;
    } catch (err) {
      console.error(`[${i + 1}] ERROR: "${prob.title}" - ${err.message}`);
      errors.push({ title: prob.title, error: err.message });
    }

    await sleep(300);
  }

  console.log('\n--- Summary ---');
  console.log(`Patched: ${matched}`);
  console.log(`Skipped (not found): ${skipped}`);
  console.log(`Errors: ${errors.length}`);
  if (errors.length > 0) {
    console.log('Error details:', errors);
  }
}

