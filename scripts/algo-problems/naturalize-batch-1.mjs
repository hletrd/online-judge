async function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function fetchAllProblems() {
  const allProblems = [];
  let page = 1;
  while (true) {
    const res = await fetch(`${API_BASE}/api/v1/problems?page=${page}&limit=50`, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) throw new Error(`Failed to fetch problems page ${page}: ${res.status}`);
    const data = await res.json();
    const items = data.problems ?? data.data ?? data;
    if (!Array.isArray(items) || items.length === 0) break;
    allProblems.push(...items);
    if (items.length < 50) break;
    page++;
  }
  return allProblems;
}

async function patchProblem(id, description) {
  const res = await fetch(`${API_BASE}/api/v1/problems/${id}`, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ description }),
  });
  return res;
}

async function main() {
  console.log('Fetching all problems from API...');
  const apiProblems = await fetchAllProblems();
  console.log(`Fetched ${apiProblems.length} problems from API.`);

  // Build title -> id map
  const titleToId = new Map();
  for (const p of apiProblems) {
    titleToId.set(p.title, p.id);
  }

  let patched = 0;
  let failed = 0;
  const errors = [];

  for (let i = 0; i < problems.length; i++) {
    const problem = problems[i];
    const naturalDesc = naturalizedDescriptions[i];
    const id = titleToId.get(problem.title);

    if (!id) {
      console.error(`[${i + 1}/50] NOT FOUND in API: "${problem.title}"`);
      failed++;
      errors.push(`Not found: ${problem.title}`);
      continue;
    }

    console.log(`[${i + 1}/50] Patching: "${problem.title}" (id=${id})`);
    try {
      const res = await patchProblem(id, naturalDesc);
      if (res.ok) {
        console.log(`  OK (${res.status})`);
        patched++;
      } else {
        const body = await res.text();
        console.error(`  FAIL (${res.status}): ${body}`);
        failed++;
        errors.push(`${problem.title}: HTTP ${res.status} - ${body}`);
      }
    } catch (err) {
      console.error(`  ERROR: ${err.message}`);
      failed++;
      errors.push(`${problem.title}: ${err.message}`);
    }

    // Delay: 2s every 10, else 400ms
    if ((i + 1) % 10 === 0 && i + 1 < problems.length) {
      console.log('  (2s pause after every 10)');
      await sleep(2000);
    } else {
      await sleep(400);
    }
  }

  console.log('\n=== DONE ===');
  console.log(`Patched: ${patched}/50`);
  console.log(`Failed:  ${failed}/50`);
  if (errors.length > 0) {
    console.log('\nErrors:');
    for (const e of errors) console.log(' -', e);
  }
}

