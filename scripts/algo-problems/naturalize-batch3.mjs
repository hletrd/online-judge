// ─── API helpers ─────────────────────────────────────────────────────────────

async function fetchAllProblems() {
  const all = [];
  let page = 1;
  while (true) {
    const res = await fetch(
      `${BASE_URL}/api/v1/problems?page=${page}&limit=50`,
      { headers: { Authorization: `Bearer ${API_KEY}` } }
    );
    if (!res.ok) throw new Error(`Fetch failed: ${res.status}`);
    const json = await res.json();
    const items = json.data ?? json.items ?? json;
    if (!Array.isArray(items) || items.length === 0) break;
    all.push(...items);
    const total = json.pagination?.total ?? json.total ?? Infinity;
    if (all.length >= total) break;
    page++;
  }
  return all;
}

async function patchProblem(id, description) {
  const res = await fetch(`${BASE_URL}/api/v1/problems/${id}`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${API_KEY}`,
    },
    body: JSON.stringify({ description }),
  });
  if (res.status === 429) {
    console.log('  Rate limited — waiting 10s...');
    await sleep(10000);
    return patchProblem(id, description);
  }
  if (!res.ok) {
    const body = await res.text();
    throw new Error(`HTTP ${res.status}: ${body}`);
  }
  return res.json();
}

// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  console.log(`Naturalized descriptions prepared: ${naturalized.length}\n`);

  // Build title -> description map
  const descMap = new Map();
  for (const p of naturalized) {
    descMap.set(p.title, p.description);
  }

  // Fetch remote problems
  console.log('Fetching problems from API...');
  const remote = await fetchAllProblems();
  console.log(`Found ${remote.length} problems on server\n`);

  let ok = 0, fail = 0, skip = 0;
  const failures = [];

  for (let i = 0; i < remote.length; i++) {
    const rp = remote[i];
    const newDesc = descMap.get(rp.title);

    if (!newDesc) {
      skip++;
      continue;
    }

    try {
      await patchProblem(rp.id, newDesc);
      ok++;
      console.log(`[${ok + fail}/${naturalized.length}] OK  ${rp.title}`);
    } catch (e) {
      fail++;
      failures.push({ title: rp.title, error: e.message });
      console.error(`[${ok + fail}/${naturalized.length}] ERR ${rp.title} -- ${e.message}`);
    }

    // Rate limiting: pause every 10 patches, 400ms otherwise
    if ((ok + fail) % 10 === 0) {
      await sleep(2000);
    } else {
      await sleep(400);
    }
  }

  console.log(`\n${'='.repeat(50)}`);
  console.log(`Patched: ${ok}   Failed: ${fail}   Skipped: ${skip}`);

  if (failures.length > 0) {
    console.log('\nFailed problems:');
    for (const f of failures) {
      console.log(`  ${f.title}: ${f.error}`);
    }
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
