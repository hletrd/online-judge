async function fetchAllProblems() {
  const allProblems = [];
  let page = 1;
  while (true) {
    const res = await fetch(`${API_BASE}/api/v1/problems?page=${page}&limit=50`, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) throw new Error(`GET /api/v1/problems?page=${page} failed: ${res.status}`);
    const data = await res.json();
    const items = data.problems ?? data.data ?? data;
    if (!Array.isArray(items) || items.length === 0) break;
    allProblems.push(...items);
    if (items.length < 50) break;
    page++;
  }
  return allProblems;
}

async function patchProblem(id, description) {
  const res = await fetch(`${API_BASE}/api/v1/problems/${id}`, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ description }),
  });
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`PATCH /api/v1/problems/${id} failed: ${res.status} - ${text}`);
  }
  return res.json();
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function main() {
  console.log('Fetching all problems from API...');
  const apiProblems = await fetchAllProblems();
  console.log(`Fetched ${apiProblems.length} problems from API.`);

  // Build title -> id map
  const titleToId = new Map();
  for (const p of apiProblems) {
    titleToId.set(p.title, p.id);
  }

  let updated = 0;
  let skipped = 0;
  const errors = [];

  for (let i = 0; i < problems.length; i++) {
    const problem = problems[i];
    const newDesc = naturalizedDescriptions[i];
    const id = titleToId.get(problem.title);

    if (!id) {
      console.warn(`[${i + 1}/50] SKIP - title not found in API: "${problem.title}"`);
      skipped++;
      continue;
    }

    try {
      await patchProblem(id, newDesc);
      updated++;
      console.log(`[${i + 1}/50] OK   - "${problem.title}" (id=${id})`);
    } catch (err) {
      errors.push({ index: i + 1, title: problem.title, error: err.message });
      console.error(`[${i + 1}/50] ERR  - "${problem.title}": ${err.message}`);
    }

    // Rate limiting: 400ms delay, extra 2s every 10 requests
    if ((i + 1) % 10 === 0 && i + 1 < problems.length) {
      console.log(`  [rate limit pause 2s after ${i + 1} requests]`);
      await sleep(2000);
    } else {
      await sleep(400);
    }
  }

  console.log('\n=== Summary ===');
  console.log(`Updated: ${updated}`);
  console.log(`Skipped: ${skipped}`);
  console.log(`Errors:  ${errors.length}`);
  if (errors.length > 0) {
    console.log('Error details:');
    for (const e of errors) {
      console.log(`  [${e.index}] ${e.title}: ${e.error}`);
    }
  }
}

