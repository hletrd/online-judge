// ─── Fetch all problems from API ────────────────────────────────────────────

async function fetchProblems() {
  const all = [];
  let page = 1;
  while (true) {
    const res = await fetch(`${BASE_URL}/api/v1/problems?page=${page}&limit=50`, {
      headers: { Authorization: `Bearer ${API_KEY}` },
    });
    if (!res.ok) throw new Error(`Fetch failed: ${res.status} ${await res.text()}`);
    const data = await res.json();
    const arr = data.data ?? data.problems ?? data;
    if (!Array.isArray(arr) || arr.length === 0) break;
    all.push(...arr);
    if (arr.length < 50) break;
    page++;
  }
  return all;
}

async function patchProblem(id, description) {
  const res = await fetch(`${BASE_URL}/api/v1/problems/${id}`, {
    method: 'PATCH',
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({ description }),
  });
  if (!res.ok) {
    const body = await res.text();
    throw new Error(`PATCH ${id} failed: ${res.status} ${body}`);
  }
  return res.json();
}

function sleep(ms) {
  return new Promise(r => setTimeout(r, ms));
}

